    fn parse_order(&mut self, mut order: Value, mut market: Value) -> Value {
        //
        // createOrder, fetchOpenOrders, fetchClosedOrders, cancelOrder, fetchOrder, fetchClosedOrder
        //
        //     {
        //         "id": 828680665,
        //         "currency_pair_id": 1,
        //         "currency_pair_name": "NXT_BTC",
        //         "price": "0.011384",
        //         "trigger_price": 0.011385,
        //         "initial_amount": "13.942",
        //         "processed_amount": "3.724", // missing in fetchClosedOrder
        //         "type": "SELL",
        //         "original_type": "STOP_LIMIT_SELL",
        //         "created": "2019-01-17 10:14:48",
        //         "timestamp": "1547720088",
        //         "status": "PARTIAL"
        //         // fetchClosedOrder only
        //         "trades": [
        //             {
        //                 "id": 658745,
        //                 "buy_order_id": 658745,
        //                 "sell_order_id": 828680665,
        //                 "price": 0.012285,
        //                 "amount": 6.35,
        //                 "trade_type": "SELL",
        //                 "timestamp": "1538737692"
        //             }
        //         ],
        //         // fetchClosedOrder only
        //         "fees": [
        //             {
        //                 "id": 1234567,
        //                 "currency_id": 1,
        //                 "amount": 0.00025,
        //                 "timestamp": "1548149238"
        //             }
        //         ]
        //     }
        //
        let mut id: Value = self.safe_string(order.clone(), Value::from("id"), Value::Undefined);
        let mut status: Value = Stex::parse_order_status(self, self.safe_string(order.clone(), Value::from("status"), Value::Undefined));
        let mut market_id: Value = self.safe_string_2(order.clone(), Value::from("currency_pair_id"), Value::from("currency_pair_name"), Value::Undefined);
        let mut symbol: Value = Stex::safe_symbol(self, market_id.clone(), market.clone(), Value::from("_"));
        let mut timestamp: Value = self.safe_timestamp(order.clone(), Value::from("timestamp"), Value::Undefined);
        let mut price: Value = Stex::safe_number(self, order.clone(), Value::from("price"), Value::Undefined);
        let mut amount: Value = Stex::safe_number(self, order.clone(), Value::from("initial_amount"), Value::Undefined);
        let mut filled: Value = Stex::safe_number(self, order.clone(), Value::from("processed_amount"), Value::Undefined);
        let mut remaining: Value = Value::Undefined;
        let mut cost: Value = Value::Undefined;
        if filled.clone().is_nonnullish() {
            if amount.clone().is_nonnullish() {
                remaining = amount.clone() - filled.clone();
                if self.get("options".into()).get(Value::from("parseOrderToPrecision")).is_truthy() {
                    remaining = parse_float(Stex::amount_to_precision(self, symbol.clone(), remaining.clone()));
                };
                remaining = Math::max(remaining.clone(), Value::from(0));
            };
            if price.clone().is_nonnullish() {
                if cost.clone().is_nullish() {
                    cost = price.clone() * filled.clone();
                };
            };
        };
        let mut r#type: Value = self.safe_string(order.clone(), Value::from("original_type"), Value::Undefined);
        if r#type.clone() == Value::from("BUY") || r#type.clone() == Value::from("SELL") {
            r#type = Value::Undefined;
        };
        let mut side: Value = self.safe_string_lower(order.clone(), Value::from("type"), Value::Undefined);
        let mut raw_trades: Value = self.safe_value(order.clone(), Value::from("trades"), Value::Undefined);
        let mut trades: Value = Value::Undefined;
        if raw_trades.clone().is_nonnullish() {
            trades = Stex::parse_trades(self, raw_trades.clone(), market.clone(), Value::Undefined, Value::Undefined, Value::Json(normalize(&Value::Json(json!({
                "symbol": symbol,
                "order": id
            }))).unwrap()));
        };
        let mut stop_price: Value = Stex::safe_number(self, order.clone(), Value::from("trigger_price"), Value::Undefined);
        let mut result: Value = Value::Json(normalize(&Value::Json(json!({
            "info": order,
            "id": id,
            "clientOrderId": Value::Undefined,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "lastTradeTimestamp": Value::Undefined,
            "symbol": symbol,
            "type": r#type,
            "timeInForce": Value::Undefined,
            "postOnly": Value::Undefined,
            "side": side,
            "price": price,
            "stopPrice": stop_price,
            "amount": amount,
            "cost": cost,
            "average": Value::Undefined,
            "filled": filled,
            "remaining": remaining,
            "status": status,
            "trades": trades
        }))).unwrap());
        let mut fees: Value = self.safe_value(order.clone(), Value::from("fees"), Value::Undefined);
        if fees.clone().is_nullish() {
            result.set("fee".into(), Value::Undefined);
        } else {
            let mut num_fees: Value = fees.len().into();
            if num_fees.clone() > Value::from(0) {
                result.set("fees".into(), Value::new_array());
                let mut i: usize = 0;
                while i < fees.len() {
                    let mut fee_cost: Value = Stex::safe_number(self, fees.get(i.into()), Value::from("amount"), Value::Undefined);
                    if fee_cost.clone().is_nonnullish() {
                        let mut fee_currency_id: Value = self.safe_string(fees.get(i.into()), Value::from("currency_id"), Value::Undefined);
                        let mut fee_currency_code: Value = Stex::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
                        result.get(Value::from("fees")).push(Value::Json(normalize(&Value::Json(json!({
                            "cost": fee_cost,
                            "currency": fee_currency_code
                        }))).unwrap()));
                    };
                    i += 1;
                };
            } else {
                result.set("fee".into(), Value::Undefined);
            };
        };
        return result.clone();
    }

