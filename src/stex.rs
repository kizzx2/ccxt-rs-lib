    fn parse_transaction(&self, mut transaction: Value, mut currency: Value) -> Value {
        //
        // fetchDeposit & fetchDeposits
        //
        //     {
        //         "id": 123654789,
        //         "currency_id": 1,
        //         "currency_code": "BTC",
        //         "deposit_fee_currency_id": 1,
        //         "deposit_fee_currency_code": "BTC",
        //         "amount": 0.25,
        //         "fee": 0.00025,
        //         "txid": "qwertyuhgfdsasdfgh",
        //         "protocol_id": 0,
        //         "deposit_status_id": 1,
        //         "status": "PROCESSING",
        //         "status_color": "#BC3D51",
        //         "created_at": "2018-11-28 12:32:08",
        //         "timestamp": "1543409389",
        //         "confirmations": "1 of 2"
        //     }
        //
        // fetchWithdrawal && fetchWithdrawals
        //
        //     {
        //         "id": 65899,
        //         "amount": "0.00600000",
        //         "currency_id": 1,
        //         "currency_code": "BTC",
        //         "fee": "0.00400000",
        //         "fee_currency_id": 1,
        //         "fee_currency_code": "BTC",
        //         "withdrawal_status_id": 1,
        //         "status": "Not Confirmed",
        //         "status_color": "#BC3D51",
        //         "created_at": "2019-01-21 09:36:05",
        //         "created_ts": "1548063365",
        //         "updated_at": "2019-01-21 09:36:05",
        //         "updated_ts": "1548063365",
        //         "txid": null,
        //         "protocol_id": 0,
        //         "withdrawal_address": {
        //             "address": "0X12WERTYUIIJHGFVBNMJHGDFGHJ765SDFGHJ",
        //             "address_name": "Address",
        //             "additional_address_parameter": "qwertyuiopasdfghjkl",
        //             "additional_address_parameter_name": "Destination Tag",
        //             "notification": "",
        //             "protocol_id": 10,
        //             "protocol_name": "Tether OMNI",
        //             "supports_new_address_creation": false
        //         }
        //     }
        //
        let mut id: Value = self.safe_string(transaction.clone(), Value::from("id"), Value::Undefined);
        let mut withdrawal_address: Value = self.safe_value(transaction.clone(), Value::from("withdrawal_address"), Value::new_object());
        let mut address: Value = self.safe_string(withdrawal_address.clone(), Value::from("address"), Value::Undefined);
        let mut tag: Value = self.safe_string(withdrawal_address.clone(), Value::from("additional_address_parameter"), Value::Undefined);
        let mut currency_id: Value = self.safe_string(transaction.clone(), Value::from("currency_id"), Value::Undefined);
        let mut code: Value = Value::Undefined;
        if self.get("currencies_by_id".into()).contains_key(currency_id.clone()) {
            currency = self.get("currencies_by_id".into()).get(currency_id.clone());
        } else {
            code = Stex::common_currency_code(self, self.safe_string(transaction.clone(), Value::from("currency_code"), Value::Undefined));
        };
        if code.clone().is_nullish() && currency.clone().is_nonnullish() {
            code = currency.get(Value::from("code"));
        };
        let mut r#type: Value = if transaction.contains_key(Value::from("deposit_status_id")) { Value::from("deposit") } else { Value::from("withdrawal") };
        let mut amount: Value = Stex::safe_number(self, transaction.clone(), Value::from("amount"), Value::Undefined);
        let mut status: Value = Stex::parse_transaction_status(self, self.safe_string_lower(transaction.clone(), Value::from("status"), Value::Undefined));
        let mut timestamp: Value = self.safe_timestamp_2(transaction.clone(), Value::from("timestamp"), Value::from("created_ts"), Value::Undefined);
        let mut updated: Value = self.safe_timestamp(transaction.clone(), Value::from("updated_ts"), Value::Undefined);
        let mut txid: Value = self.safe_string(transaction.clone(), Value::from("txid"), Value::Undefined);
        let mut fee: Value = Value::Undefined;
        let mut fee_cost: Value = Stex::safe_number(self, transaction.clone(), Value::from("fee"), Value::Undefined);
        if fee_cost.clone().is_nonnullish() {
            let mut fee_currency_id: Value = self.safe_string_2(transaction.clone(), Value::from("fee_currency_id"), Value::from("deposit_fee_currency_id"), Value::Undefined);
            let mut fee_currency_code: Value = Stex::safe_currency_code(self, fee_currency_id.clone(), Value::Undefined);
            fee = Value::Json(normalize(&Value::Json(json!({
                "cost": fee_cost,
                "currency": fee_currency_code
            }))).unwrap());
        };
        let mut network: Value = self.safe_string(withdrawal_address.clone(), Value::from("protocol_name"), Value::Undefined);
        return Value::Json(normalize(&Value::Json(json!({
            "info": transaction,
            "id": id,
            "txid": txid,
            "timestamp": timestamp,
            "datetime": self.iso8601(timestamp.clone()),
            "network": network,
            "addressFrom": Value::Undefined,
            "address": address,
            "addressTo": address,
            "tagFrom": Value::Undefined,
            "tag": tag,
            "tagTo": tag,
            "type": r#type,
            "amount": amount,
            "currency": code,
            "status": status,
            "updated": updated,
            "fee": fee
        }))).unwrap());
    }

