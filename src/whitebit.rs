    /// Returns a list of [order structures](https://docs.ccxt.com/en/latest/manual.html#order-structure)
    ///
    /// Fetches information on multiple closed orders made by the user
    ///
    /// # Arguments
    ///
    /// * `symbol` {string|undefined} - unified market symbol of the market orders were made in
    /// * `since` {int|undefined} - the earliest time in ms to fetch orders for
    /// * `limit` {int|undefined} - the maximum number of  orde structures to retrieve
    /// * `params` {object} - extra parameters specific to the whitebit api endpoint
    async fn fetch_closed_orders(&mut self, mut symbol: Value, mut since: Value, mut limit: Value, mut params: Value) -> Value {
        params = params.or_default(Value::new_object());
        Whitebit::load_markets(self, Value::Undefined, Value::Undefined).await;
        let mut request: Value = Value::new_object();
        let mut market: Value = Value::Undefined;
        if symbol.clone().is_nonnullish() {
            market = Whitebit::market(self, symbol.clone());
            symbol = market.get(Value::from("symbol"));
            request.set("market".into(), market.get(Value::from("id")));
        };
        if limit.clone().is_nonnullish() {
            request.set("limit".into(), limit.clone());
        };
        // default 50 max 100
        let mut response: Value = Whitebit::dispatch(self, "v4PrivatePostTradeAccountOrderHistory".into(), extend_2(request.clone(), params.clone()), Value::Undefined).await;
        //
        //     {
        //         "BTC_USDT": [
        //             {
        //                 "id": 160305483,
        //                 "clientOrderId": "customId11",
        //                 "time": 1594667731.724403,
        //                 "side": "sell",
        //                 "role": 2, // 1 = maker, 2 = taker
        //                 "amount": "0.000076",
        //                 "price": "9264.21",
        //                 "deal": "0.70407996",
        //                 "fee": "0.00070407996"
        //             },
        //         ],
        //     }
        //
        let mut market_ids: Value = Object::keys(response.clone());
        let mut results: Value = Value::new_array();
        let mut i: usize = 0;
        while i < market_ids.len() {
            let mut market_id: Value = market_ids.get(i.into());
            let mut market: Value = Whitebit::safe_market(self, market_id.clone(), Value::Undefined, Value::from("_"));
            let mut orders: Value = response.get(market_id.clone());
            let mut j: usize = 0;
            while j < orders.len() {
                let mut order: Value = Whitebit::parse_order(self, orders.get(j.into()), market.clone());
                results.push(extend_2(order.clone(), Value::Json(normalize(&Value::Json(json!({
                    "status": "closed"
                }))).unwrap())));
                j += 1;
            };
            i += 1;
        };
        results = self.sort_by(results.clone(), Value::from("timestamp"), Value::Undefined, Value::Undefined);
        results = Whitebit::filter_by_symbol_since_limit(self, results.clone(), symbol.clone(), since.clone(), limit.clone(), Value::Undefined);
        return results.clone();
    }

